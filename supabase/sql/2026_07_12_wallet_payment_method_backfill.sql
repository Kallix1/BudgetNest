-- Дата: 2026-07-12 · Статус: backfill-ът вече е изпълнен на живо
--
-- ИСТОРИЯ НА ПРОБЛЕМА
-- Транзакциите, създадени ПРЕДИ въвеждането на портфейла
-- (карта / в брой), имаха payment_method = NULL. Тригерът
-- transactions_wallet_sync -> apply_transaction_to_wallet()
-- игнорира редове с NULL метод (RETURN веднага, без да пипа
-- баланса), затова тези суми участваха в "Приходи/Разходи
-- този месец", но липсваха от "В картата" / "В брой" / "Общо".
--
-- КАК Е ПОПРАВЕНО (вече се случи)
--   UPDATE public.transactions
--   SET payment_method = 'card'
--   WHERE payment_method IS NULL;
--
-- Понеже тригерът обработва UPDATE като "извади стария принос,
-- добави новия", а старият принос на NULL ред е бил нула, това
-- UPDATE е добавило старите суми в card_balance точно както при
-- съвсем нова транзакция — без дублиране. Потвърдено: никое
-- домакинство не беше въвеждало ръчен начален баланс в
-- „Портфейл“ преди изпълнението, така че няма риск от двойно
-- броене. Балансите вече отразяват реалната история.
--
-- ОСТАВА: защита занапред, за да не се появи NULL отново.
-- Изпълнете само това, ако вече не е изпълнено:
-- ============================================================

-- Нов запис без изрично зададен метод получава 'card' по подразбиране.
ALTER TABLE public.transactions
ALTER COLUMN payment_method SET DEFAULT 'card';

-- Заключваме колоната напълно — вече всички пътища за запис
-- (бърза транзакция, редакция, шаблони, периодични правила)
-- подават payment_method изрично, така че това е безопасно.
ALTER TABLE public.transactions
ALTER COLUMN payment_method SET NOT NULL;
